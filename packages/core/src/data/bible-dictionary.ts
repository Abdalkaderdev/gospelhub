// Helper functions
export const getAllDictionaryEntries = (): DictionaryEntry[] => {
  return BIBLE_DICTIONARY;
};

export const getEntryById = (id: string): DictionaryEntry | undefined => {
  return BIBLE_DICTIONARY.find(entry => entry.id === id);
};

export const getEntriesByCategory = (category: string): DictionaryEntry[] => {
  return BIBLE_DICTIONARY.filter(entry => entry.category === category);
};

export const searchDictionary = (query: string): DictionaryEntry[] => {
  const lowercaseQuery = query.toLowerCase();
  return BIBLE_DICTIONARY.filter(entry => 
    entry.term.toLowerCase().includes(lowercaseQuery) ||
    entry.definition.toLowerCase().includes(lowercaseQuery) ||
    entry.practicalApplication.toLowerCase().includes(lowercaseQuery) ||
    entry.relatedTerms.some(term => term.toLowerCase().includes(lowercaseQuery))
  );
};

export const getRandomEntry = (): DictionaryEntry => {
  const randomIndex = Math.floor(Math.random() * BIBLE_DICTIONARY.length);
  return BIBLE_DICTIONARY[randomIndex];
};

export const getAlphabeticalEntries = (): DictionaryEntry[] => {
  return [...BIBLE_DICTIONARY].sort((a, b) => a.term.localeCompare(b.term));
};